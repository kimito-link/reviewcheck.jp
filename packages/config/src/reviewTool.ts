/**
 * 口コミ作成サポートツールの設定（業種別プリセット）。
 *
 * コンセプト（removal「レビューブースト」より）:
 * 来店客「本人」が質問にタップで答えるだけで、自然な口コミ文の“下書き”が完成する。
 * 文章は本人が自由に編集して投稿する＝やらせ・サクラではなく、正規の口コミ獲得支援。
 * 投稿は任意・謝礼や特典は一切なし（ステマ規制・Googleポリシー遵守）。
 */

export interface RTOption {
  /** ボタンに表示するラベル */
  label: string;
  /** この選択で口コミ文に差し込む文のバリエーション（ランダムで1つ採用） */
  variants: string[];
}

export interface RTQuestion {
  id: string;
  /** 質問文（\n で改行可） */
  text: string;
  options: RTOption[];
}

export interface RTPreset {
  id: string;
  /** 業種名（選択UI用） */
  label: string;
  /** ロゴ代わりの絵文字 */
  emoji: string;
  /** テーマカラー（HEX） */
  color: string;
  /** 「◯◯を利用しました」等の言い回しに使う動詞表現 */
  verb: string;
  questions: RTQuestion[];
}

